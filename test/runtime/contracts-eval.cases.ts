it("verification timeout kills child processes spawned by the command shell", async () => {
    const cwd = tempWorkspace();
    const childPidFile = path.join(cwd, "child.pid");
    const script = [
      'const { spawn } = require("node:child_process");',
      'const fs = require("node:fs");',
      'const child = spawn(process.execPath, ["-e", "process.on(\\\\\\"SIGTERM\\\\\\", () => {}); setInterval(() => {}, 1000);"], { stdio: "ignore" });',
      `fs.writeFileSync(${JSON.stringify(childPidFile)}, String(child.pid));`,
      "setInterval(() => {}, 1000);",
    ].join(" ");
    const command = `node -e ${JSON.stringify(script)}`;
    const loaded = loadConfig(cwd);
    loaded.config.harness.allowed_commands = [command];
    loaded.config.harness.command_timeout_seconds = 1;
    const result = await runVerificationPlan(cwd, loaded.config, [command]);
    expect(result.passed).toBe(false);
    expect(result.results[0].timed_out).toBe(true);
    expect(fs.existsSync(childPidFile)).toBe(true);
    const childPid = Number(fs.readFileSync(childPidFile, "utf8"));
    try {
      for (let attempt = 0; attempt < 30 && processAlive(childPid); attempt += 1) {
        await new Promise((resolve) => setTimeout(resolve, 100));
      }
      expect(processAlive(childPid)).toBe(false);
    } finally {
      if (processAlive(childPid)) {
        try {
          process.kill(childPid, "SIGKILL");
        } catch {
          // Already gone.
        }
      }
    }
  });

